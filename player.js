function drawSprite(spr, ox, oy, flip) {
  ctx.fillStyle = '#fff';
  const sw = 15;
  for (const [sx, sy, sw2] of spr) {
    const rx = flip ? (sw - sx - sw2) : sx;
    ctx.fillRect(ox + rx, oy + sy, sw2, 1);
  }
}

